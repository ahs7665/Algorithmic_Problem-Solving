class Result {

    public static long getWays(int n, List<Long> c) {
    // Write your code here
        long [] solution = new long[n+1];
        solution[0] = (long)1;
        for(long coin: c){
            for(int j = (int)coin; j <= n; j++){
                solution[j] = solution[j] + solution[(int)(j-coin)];
            }
        }
        return solution[n];
    }

}

